from random import randrange

import re
import random

def _do_file(action, text=None):
       with open("11111.txt", action) as file:
              if action == 'w':
                     print(text, file=file)
              else:
                     return file.read()

_do_file("w", ''.join(str(i) for i in [random.randint(1, 9) for x in range(2500)]))

def find_longest_rep(s):

    result = max(re.findall(r'((\d+?)\2+)', s), key=lambda t: len(t[0]))
    return result[0]

print(find_longest_rep(_do_file("r")))